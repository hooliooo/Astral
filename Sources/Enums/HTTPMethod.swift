/**
 A String Enum that represents http methods
*/
public enum HTTPMethod: String {
    /**
     - http GET method
    */
    case GET

    /**
     - http DELETE method
    */
    case DELETE

    /**
     - http POST method
    */
    case POST

    /**
     - http PUT method
    */
    case PUT

    /**
     - http PATCH method
    */
    case PATCH
}
